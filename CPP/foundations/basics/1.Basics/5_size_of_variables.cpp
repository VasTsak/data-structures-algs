#include<iostream>
using namespace std;

int main()
{
    cout<<"short size "<<sizeof(short)<<"\n";
    cout<<"long size "<<sizeof(long)<<"\n";
    cout<<"char size "<<sizeof(char)<<"\n";
    cout<<"float size "<<sizeof(float)<<"\n";
    cout<<"double size "<<sizeof(double)<<"\n";
    cout<<"bool size "<<sizeof(bool)<<"\n";
    return 0;
}